package frc.robot.commands.AutosReal;

import edu.wpi.first.wpilibj2.command.Command;
import frc.robot.subsystems.Elevator;

/* You should consider using the more terse Command factories API instead https://docs.wpilib.org/en/stable/docs/software/commandbased/organizing-command-based.html#defining-commands */
public class ElevatorPose3 extends Command {
  /** Creates a new ElevatorPose3. */
  Elevator elevator;

  public ElevatorPose3(Elevator elevator) {
    this.elevator = elevator;

    addRequirements(elevator);
  }


  // Called when the command is initially scheduled.
  @Override
  public void initialize() {}

  // Called every time the scheduler runs while the command is scheduled.
  @Override
  public void execute() {
    elevator.runElevatorToPosition(4);
  }

  // Called once the command ends or is interrupted.
  @Override
  public void end(boolean interrupted) {}

  // Returns true when the command should end.
  @Override
  public boolean isFinished() {
    return elevator.getElePos() < 3.1 && elevator.getElePos() > 2.9;
  }
}
